# Подсчитать количество уникальных слов в тексте, игнорируя регистр и знаки припинания
# Мама мыла раму. Дочка не мыла раму, потому что дочка плохо моет раму, а мама моет раму хорошо, а не плохо
text = input("Введите текст: ").split()
text = [t.lower().strip(" ,.;:!?") for t in text]

print(text)# print(*text) - выводится без символов(точек, запятых)
l = len(set(text))
print(l)
